package com.tekwill.learning.datatypes;

public class Constants {
    public static final int ZERO_NUMBER = 0;
    public static final int ONE_NUMBER = 1;
    public static final double PI_NUMBER = Math.PI;
    public static final double E_NUMBER = Math.exp(1);
    public static final double SQUARE_ROOT_TWO = Math.sqrt(2);
    public static final double GOLDEN_RATIO = (1 + Math.sqrt(5)) / 2;

    public static void main(String[] args) {
        System.out.println("Zero: " + ZERO_NUMBER);
        System.out.println("One: " + ONE_NUMBER);
        System.out.println("Pi: " + PI_NUMBER);
        System.out.println("e: " + E_NUMBER);
        System.out.println("Square: " + SQUARE_ROOT_TWO);
        System.out.println("Golden: " + GOLDEN_RATIO);
    }
}
