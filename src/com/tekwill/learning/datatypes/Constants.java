package com.tekwill.learning.datatypes;

public class Constants {
    public static int zeroNumber = 0;
    public static int oneNumber = 1;
    public static double piNumber = Math.PI;
    public static double eNumber = Math.exp(1);
    public static double squareRootTwo = Math.sqrt(2);
    public static double goldenRatio = (1 + Math.sqrt(5)) / 2;

    public static void main(String[] args) {
        System.out.println("Zero: " + zeroNumber);
        System.out.println("One: " + oneNumber);
        System.out.println("Pi: " + piNumber);
        System.out.println("e: " + eNumber);
        System.out.println("Square: " + squareRootTwo);
        System.out.println("Golden: " + goldenRatio);
    }
}
